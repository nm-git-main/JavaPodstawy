package javaPodstawy.IntroMetody;

public class ParaInt {
    private int lewy;
    private int prawy;

    public ParaInt(int lewy, int prawy){
        this.lewy=lewy;
        this.prawy=prawy;
    }
    public int getLewy(){
        return lewy;
    }
    public int getPrawy(){
        return prawy;
    }
}

